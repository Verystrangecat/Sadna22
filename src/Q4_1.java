public class Q4_1 {
    public static void main(String[] args) {
        int n, rep=0;
        for (n=4; n<10; n=n+2){
            System.out.println(n);
            rep++;
        }
        System.out.println(rep);
        System.out.println("\t BREAK");
        int n2, rep2=0;
        for (n2=20; n2>10; n2=n2-2){
            System.out.println(n2);
            rep2++;
        }
        System.out.println(rep2);
    }
}
